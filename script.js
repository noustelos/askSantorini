function getIntentPerformanceStats(intentType) {
  const matches = conciergeAffiliates.filter((affiliate) => affiliate.type === intentType);
  const clicks = matches.reduce((total, affiliate) => total + (Number(affiliate.clicks) || 0), 0);
  const impressions = matches.reduce((total, affiliate) => total + (Number(affiliate.impressions) || 0), 0);
  const state = getAutonomousOptimizationState();
  const intentState = ensureIntentOptimizationState(state, intentType);
  const memoryValues = Object.values(intentState.affiliates || {});
  const memoryClicks = memoryValues.reduce((total, affiliate) => total + (Number(affiliate.clicks) || 0), 0);
  const memoryImpressions = memoryValues.reduce((total, affiliate) => total + (Number(affiliate.impressions) || 0), 0);
  const totalClicks = clicks + memoryClicks;
  const totalImpressions = impressions + memoryImpressions;
  const averageCtr = totalImpressions > 0 ? Math.min(totalClicks / (totalImpressions + 1), 1) : 0;

  return {
    averageCtr,
    affiliateCount: matches.length,
    totalClicks,
    totalImpressions
  };
}

function getAbTestModifier(affiliate, variant) {
  if (variant !== "B") {
    return 0;
  }

  const confidence = Math.min((Number(affiliate.impressions) || 0) / conciergeOptimizationConfig.drift.confidenceSampleSize, 1);

  return (1 - confidence) * conciergeOptimizationConfig.abTesting.explorationBoost;
}

function getDriftModifier(affiliate, intentStats) {
  const config = conciergeOptimizationConfig.drift;
  const memory = getAffiliatePerformanceMemory(affiliate.type, affiliate);
  const sheetImpressions = Number(affiliate.impressions) || 0;
  const memoryImpressions = Number(memory?.impressions) || 0;
  const impressions = sheetImpressions + memoryImpressions;
  const sheetClicks = Number(affiliate.clicks) || 0;
  const memoryClicks = Number(memory?.clicks) || 0;
  const combinedCtr = impressions > 0 ? Math.min((sheetClicks + memoryClicks) / (impressions + 1), 1) : Number(affiliate.ctr) || 0;
  const confidence = Math.min(impressions / config.confidenceSampleSize, 1);
  const relativeCtr = combinedCtr - (Number(intentStats.averageCtr) || 0);
  const rawModifier = relativeCtr * config.multiplier * confidence;

  if (impressions < config.minSamplesForDecay && rawModifier < 0) {
    return 0;
  }

  const trendModifier = memory?.trendDirection === "up" ? 0.08 : memory?.trendDirection === "down" ? -0.08 : 0;

  return clampNumber(rawModifier + trendModifier, config.minModifier, config.maxModifier);
}

function buildAffiliateScoreContext({ affiliate, intentType, userMessage, variant, seasonalMode, intentStats }) {
  const normalizedMessage = String(userMessage || "").toLowerCase();
  const weights = getIntentOptimizationWeights(intentType);
  const warmupMultiplier = getAffiliateWarmupMultiplier(intentType, affiliate);
  const tagScore = getAffiliateTags(affiliate).reduce((score, tag) => {
    return tag && normalizedMessage.includes(tag) ? score + 1 : score;
  }, 0);
  const abTestModifier = getAbTestModifier(affiliate, variant);
  const seasonalModifier = getSeasonalModifier(affiliate, seasonalMode);
  const driftModifier = getDriftModifier(affiliate, intentStats);
  const scoreBreakdown = {
    priority: affiliate.priority * weights.priority,
    ctr: affiliate.ctr * weights.ctr * warmupMultiplier,
    intentStrength: affiliate.intentStrength * weights.intentStrength,
    contextBoost: affiliate.contextBoost * weights.contextBoost,
    abTestModifier,
    seasonalModifier,
    driftModifier: driftModifier * warmupMultiplier,
    tagScore
  };
  const finalScore = Object.values(scoreBreakdown).reduce((total, value) => total + (Number(value) || 0), 0);

  return {
    affiliate,
    intent: intentType,
    variant,
    seasonalMode,
    intentStats,
    scoreBreakdown,
    finalScore,
    warmupMultiplier
  };
}

function getTrendDirection(previousCtr, currentCtr) {
  const delta = currentCtr - previousCtr;

  if (delta > 0.015) return "up";
  if (delta < -0.015) return "down";
  return "stable";
}

function getTopAndWorstAffiliateMemory(affiliates) {
  const sorted = [...affiliates].sort((a, b) => {
    if (b.ctr !== a.ctr) return b.ctr - a.ctr;
    return b.impressions - a.impressions;
  });

  return {
    top: sorted[0] || null,
    worst: sorted[sorted.length - 1] || null
  };
}

function getAffiliateDistributionMetrics(affiliates) {
  const totalImpressions = affiliates.reduce((total, affiliate) => total + (Number(affiliate.impressions) || 0), 0);
  const distribution = affiliates.map((affiliate) => ({
    affiliate_id: affiliate.affiliate_id,
    name: affiliate.name,
    impressions: Number(affiliate.impressions) || 0,
    clicks: Number(affiliate.clicks) || 0,
    ctr: Number(affiliate.ctr) || 0,
    share: totalImpressions > 0 ? (Number(affiliate.impressions) || 0) / totalImpressions : 0
  })).sort((a, b) => b.share - a.share);
  const topShare = distribution[0]?.share || 0;
  const averageCtr = distribution.length
    ? distribution.reduce((total, affiliate) => total + affiliate.ctr, 0) / distribution.length
    : 0;
  const ctrVariance = distribution.length
    ? distribution.reduce((total, affiliate) => total + ((affiliate.ctr - averageCtr) ** 2), 0) / distribution.length
    : 0;

  return {
    totalImpressions,
    activeAffiliateCount: distribution.filter((affiliate) => affiliate.impressions > 0).length,
    distribution,
    dominanceIndex: topShare,
    ctrVariance
  };
}

function limitWeightChange(previousWeights, proposedWeights) {
  const ratio = conciergeOptimizationConfig.guardrails.maxWeightChangeRatio;

  return Object.fromEntries(Object.entries(proposedWeights).map(([key, proposedValue]) => {
    const previousValue = Number(previousWeights[key] ?? getDefaultOptimizationWeights()[key]) || getDefaultOptimizationWeights()[key];
    const maxDelta = Math.max(Math.abs(previousValue) * ratio, 0.01);
    const limitedValue = clampNumber(proposedValue, previousValue - maxDelta, previousValue + maxDelta);

    return [key, clampOptimizationWeight(limitedValue)];
  }));
}

function smoothWeightHistory(intentState, weights) {
  const windowSize = conciergeOptimizationConfig.guardrails.smoothingWindow;
  const history = [...(intentState.weightHistory || []), weights].slice(-windowSize);
  const smoothedWeights = {};

  Object.keys(getDefaultOptimizationWeights()).forEach((key) => {
    smoothedWeights[key] = clampOptimizationWeight(
      history.reduce((total, item) => total + (Number(item[key]) || 0), 0) / history.length
    );
  });

  intentState.weightHistory = history;

  return smoothedWeights;
}

function getRankingSignature(affiliates) {
  return affiliates
    .slice(0, 3)
    .map((affiliate) => affiliate.affiliate_id || affiliate.name || "")
    .join("|");
}

function getRankingShiftScore(previousSignature, currentSignature) {
  if (!previousSignature || !currentSignature) {
    return 0;
  }

  const previousItems = previousSignature.split("|");
  const currentItems = currentSignature.split("|");
  const changes = currentItems.filter((item, index) => item !== previousItems[index]).length;

  return changes / Math.max(currentItems.length, 1);
}

function getSafeExplorationRate(intentType) {
  const state = getAutonomousOptimizationState();
  const intentState = ensureIntentOptimizationState(state, intentType);
  const baseRate = conciergeOptimizationConfig.autonomous.explorationRate;
  const guardrails = conciergeOptimizationConfig.guardrails;
  const boostedRate = intentState.failsafeCyclesRemaining > 0
    ? baseRate + guardrails.instabilityExplorationBoost
    : baseRate;

  return clampNumber(boostedRate, guardrails.minExplorationRate, guardrails.maxExplorationRate);
}

function updateAutonomousWeightsForIntent(intentType, intentState) {
  const guardrails = conciergeOptimizationConfig.guardrails;
  const dynamicBatchSize = getDynamicOptimizationBatchSize();
  const affiliates = Object.values(intentState.affiliates || {});
  const totalClicks = affiliates.reduce((total, affiliate) => total + (Number(affiliate.clicks) || 0), 0);
  const totalImpressions = affiliates.reduce((total, affiliate) => total + (Number(affiliate.impressions) || 0), 0);

  if (totalImpressions < dynamicBatchSize || totalImpressions < guardrails.minimumUpdateSamples) {
    return;
  }

  const currentAverageCtr = totalClicks / (totalImpressions + 1);
  const previousAverageCtr = Number(intentState.previousAverageCtr) || 0;
  const trendDirection = getTrendDirection(previousAverageCtr, currentAverageCtr);
  const weights = {
    ...getDefaultOptimizationWeights(),
    ...(intentState.weights || {})
  };
  const previousWeights = { ...weights };
  const { top, worst } = getTopAndWorstAffiliateMemory(affiliates);
  const distributionMetrics = getAffiliateDistributionMetrics(affiliates);
  const variantPerformance = Object.entries(intentState.variants || {}).map(([variant, value]) => ({
    variant,
    impressions: Number(value.impressions) || 0,
    clicks: Number(value.clicks) || 0,
    ctr: Number(value.impressions) > 0 ? (Number(value.clicks) || 0) / ((Number(value.impressions) || 0) + 1) : 0
  })).sort((a, b) => b.ctr - a.ctr);
  const adjustment = conciergeOptimizationConfig.autonomous.learningRate;
  const previousRankingSignature = intentState.lastRankingSignature || "";
  const currentRankingSignature = getRankingSignature(affiliates);
  const rankingShiftScore = getRankingShiftScore(previousRankingSignature, currentRankingSignature);
  const ctrSpike = Math.abs(currentAverageCtr - previousAverageCtr) > guardrails.ctrSpikeThreshold;
  const dominanceDetected = distributionMetrics.dominanceIndex > guardrails.dominanceShareLimit;
  const diversityTooLow = distributionMetrics.activeAffiliateCount > 0
    && distributionMetrics.activeAffiliateCount < guardrails.minimumActiveAffiliates;
  const rankingShiftDetected = rankingShiftScore > guardrails.rankingShiftThreshold;
  const instabilityDetected = ctrSpike || dominanceDetected || diversityTooLow || rankingShiftDetected;

  if (intentState.failsafeCyclesRemaining > 0) {
    intentState.failsafeCyclesRemaining -= 1;
    intentState.weights = {
      ...getDefaultOptimizationWeights(),
      ...(intentState.lastStableWeights || intentState.weights || {})
    };
    intentState.previousAverageCtr = currentAverageCtr;

    console.warn("AskSantorini autonomous optimization safe mode active:", {
      intent: intentType,
      failsafeCyclesRemaining: intentState.failsafeCyclesRemaining,
      weights: intentState.weights,
      distributionMetrics,
      rankingShiftScore
    });
    return;
  }

  const effectiveAdjustment = instabilityDetected ? adjustment / 2 : adjustment;

  if (trendDirection === "up") {
    weights.ctr = clampOptimizationWeight(weights.ctr + effectiveAdjustment);
    weights.intentStrength = clampOptimizationWeight(weights.intentStrength + effectiveAdjustment / 2);
    weights.priority = clampOptimizationWeight(weights.priority - effectiveAdjustment / 2);
  } else if (trendDirection === "down") {
    weights.ctr = clampOptimizationWeight(weights.ctr - effectiveAdjustment / 2);
    weights.contextBoost = clampOptimizationWeight(weights.contextBoost + effectiveAdjustment / 2);
    weights.priority = clampOptimizationWeight(weights.priority + effectiveAdjustment / 4);
  } else {
    weights.ctr = clampOptimizationWeight(weights.ctr + effectiveAdjustment / 5);
  }

  const variantA = variantPerformance.find((variant) => variant.variant === "A");
  const variantB = variantPerformance.find((variant) => variant.variant === "B");

  if (variantA && variantB && variantA.impressions >= 5 && variantB.impressions >= 5) {
    if (variantB.ctr > variantA.ctr + 0.02) {
      weights.ctr = clampOptimizationWeight(weights.ctr + effectiveAdjustment / 2);
      weights.priority = clampOptimizationWeight(weights.priority - effectiveAdjustment / 4);
    } else if (variantA.ctr > variantB.ctr + 0.02) {
      weights.priority = clampOptimizationWeight(weights.priority + effectiveAdjustment / 4);
      weights.ctr = clampOptimizationWeight(weights.ctr - effectiveAdjustment / 4);
    }
  }

  const limitedWeights = limitWeightChange(previousWeights, weights);
  const smoothedWeights = smoothWeightHistory(intentState, limitedWeights);

  if (instabilityDetected) {
    intentState.failsafeCyclesRemaining = guardrails.failsafeCycles;
    intentState.weights = {
      ...getDefaultOptimizationWeights(),
      ...(intentState.lastStableWeights || smoothedWeights)
    };
  } else {
    intentState.weights = smoothedWeights;
    intentState.lastStableWeights = smoothedWeights;
  }

  intentState.previousAverageCtr = currentAverageCtr;
  intentState.lastRankingSignature = currentRankingSignature;

  askSantoriniTrafficLog("AskSantorini autonomous optimization weights updated:", {
    intent: intentType,
    weights: intentState.weights,
    proposedWeights: weights,
    limitedWeights,
    smoothedWeights,
    weightChangePerCycle: Object.fromEntries(Object.keys(intentState.weights).map((key) => [
      key,
      (Number(intentState.weights[key]) || 0) - (Number(previousWeights[key]) || 0)
    ])),
    trendDirection,
    currentAverageCtr,
    previousAverageCtr,
    topAffiliate: top,
    worstAffiliate: worst,
    variantPerformance,
    distributionMetrics,
    explorationRate: getSafeExplorationRate(intentType),
    exploitationRate: 1 - getSafeExplorationRate(intentType),
    dominanceIndex: distributionMetrics.dominanceIndex,
    ctrVariance: distributionMetrics.ctrVariance,
    dynamicBatchSize,
    rankingShiftScore,
    instabilityDetected,
    safeModeActivated: instabilityDetected
  });
}

function updateAutonomousOptimizationCycle(state) {
  Object.entries(state.intents || {}).forEach(([intentType, intentState]) => {
    updateAutonomousWeightsForIntent(intentType, intentState);
  });
}

function recordAffiliatePerformance({ intent, affiliate, eventType, optimizationContext = null }) {
  if (!affiliate || !intent || !["impression", "click"].includes(eventType)) {
    return;
  }

  const state = getAutonomousOptimizationState();
  const intentState = ensureIntentOptimizationState(state, intent);
  const affiliateKey = getAffiliateOptimizationKey(affiliate);

  if (!affiliateKey) {
    return;
  }

  const current = intentState.affiliates[affiliateKey] || {
    affiliate_id: affiliateKey,
    name: affiliate.name || "",
    impressions: 0,
    clicks: 0,
    ctr: 0,
    previousCtr: 0,
    trendDirection: "stable"
  };

  current.previousCtr = Number(current.ctr) || 0;

  if (eventType === "impression") {
    current.impressions += 1;
  }

  if (eventType === "click") {
    current.clicks += 1;
  }

  current.ctr = current.impressions > 0 ? current.clicks / (current.impressions + 1) : 0;
  current.trendDirection = getTrendDirection(current.previousCtr, current.ctr);
  intentState.affiliates[affiliateKey] = current;

  const variant = optimizationContext?.variant;

  if (variant) {
    const variantState = intentState.variants[variant] || {
      impressions: 0,
      clicks: 0
    };

    if (eventType === "impression") {
      variantState.impressions += 1;
    }

    if (eventType === "click") {
      variantState.clicks += 1;
    }

    intentState.variants[variant] = variantState;
  }

  state.eventCount += 1;

  const dynamicBatchSize = getDynamicOptimizationBatchSize();

  if (state.eventCount % dynamicBatchSize === 0) {
    updateAutonomousOptimizationCycle(state);
  }

  setAutonomousOptimizationState(state);

  askSantoriniTrafficLog("AskSantorini autonomous affiliate performance memory:", {
    intent,
    eventType,
    affiliate: current,
    variants: intentState.variants,
    eventCount: state.eventCount,
    trafficSnapshot: getTrafficSnapshot(),
    dynamicBatchSize
  });
}

function getNextConciergeRotationIndex(intentType, matchCount) {
  if (!intentType || matchCount < 2) return 0;

  const state = getConciergeRotationState();
  const currentIndex = Number.isFinite(Number(state[intentType])) ? Number(state[intentType]) : 0;
  const selectedIndex = currentIndex % matchCount;

  state[intentType] = (selectedIndex + 1) % matchCount;
  setConciergeRotationState(state);

  return selectedIndex;
}

function shouldExploreAffiliate(intentType, userMessage) {
  const sessionId = getConciergeSessionId();
  const hash = Math.abs(hashString(`${sessionId}:${intentType}:${userMessage}:${getConciergeRotationState()[intentType] || 0}`));
  const bucket = (hash % 100) / 100;

  return bucket < getSafeExplorationRate(intentType);
}

function chooseConciergeAffiliate(intentType, userMessage) {
  latestConciergeSelectionContext = null;

  if (!intentType) return null;

  const activeEntity = getActiveEntity();
  if (activeEntity?.type === intentType) {
    latestConciergeSelectionContext = {
      affiliate: activeEntity,
      intentType,
      variant: getConciergeVariant(intentType),
      seasonalMode: getSeasonalMode(),
      scoreBreakdown: { priority: activeEntity.priority || 0 },
      finalScore: activeEntity.priority || 0,
      isExplorationPick: false,
      explorationRate: 0
    };
    return hydrateTruthLayerEntity(activeEntity.entityId, "session_memory_selection") || activeEntity;
  }

  const variant = getConciergeVariant(intentType);
  const seasonalMode = getSeasonalMode();
  const matches = conciergeAffiliates
    .filter((affiliate) => affiliate.type === intentType)
    .map((affiliate) => ({
      affiliate,
      variant,
      seasonalMode,
      scoreBreakdown: { priority: affiliate.priority || 0 },
      finalScore: affiliate.priority || 0,
      isExplorationPick: false,
      explorationRate: 0
    }))
    .sort((a, b) => {
      if (b.finalScore !== a.finalScore) return b.finalScore - a.finalScore;
      return String(a.affiliate.name || "").localeCompare(String(b.affiliate.name || ""));
    });

  if (!matches.length) return null;

  latestConciergeSelectionContext = matches[0] || null;

  if (latestConciergeSelectionContext) {
    latestConciergeSelectionContext.isExplorationPick = false;
    latestConciergeSelectionContext.explorationRate = 0;
    const hydratedSelection = hydrateTruthLayerEntity(latestConciergeSelectionContext.affiliate.entityId, "new_selection");
    latestConciergeSelectionContext.affiliate = hydratedSelection || latestConciergeSelectionContext.affiliate;
    setActiveEntity(latestConciergeSelectionContext.affiliate);
  }

  askSantoriniTrafficLog("AskSantorini concierge optimization selection:", {
    intent: intentType,
    variant,
    seasonalMode,
    isExplorationPick: false,
    explorationRate: 0,
    exploitationRate: 1,
    selectedAffiliate: latestConciergeSelectionContext?.affiliate?.name || "",
    scoreBreakdown: latestConciergeSelectionContext?.scoreBreakdown || {},
    finalScore: latestConciergeSelectionContext?.finalScore || 0,
    rankedAffiliates: matches.slice(0, 3).map((match) => ({
      name: match.affiliate.name,
      finalScore: match.finalScore,
      scoreBreakdown: match.scoreBreakdown
    }))
  });

  return latestConciergeSelectionContext?.affiliate || null;
}

function getConciergeAffiliate(userMessage) {
  const intent = detectConciergeIntent(userMessage);
  return chooseConciergeAffiliate(intent, userMessage);
}

function buildConciergePrompt(userMessage, affiliate) {
  const affiliateTags = affiliate ? getAffiliateTags(affiliate).join(", ") : "";
  const hasVerifiedContact = Boolean(
    affiliate
    && affiliate.entityId
    && (affiliate.phone || affiliate.website || affiliate.websiteUrl || affiliate.mapsUrl)
  );
  const selectedAffiliateContext = affiliate
    ? [
        "Selected affiliate context:",
        `Entity ID: ${affiliate.entityId || "(none)"}`,
        `Name: ${affiliate.name}`,
        `Type: ${affiliate.type}`,
        affiliateTags ? `Tags: ${affiliateTags}` : "",
        `Verified contact data available: ${hasVerifiedContact ? "yes" : "no"}`
      ].filter(Boolean).join("\n")
    : "Selected affiliate context: none\nVerified contact data available: no";

  const contextualRule = hasVerifiedContact
    ? conciergeRuleWithVerifiedEntity
    : conciergeRuleWithoutVerifiedEntity;

  return [
    conciergeSystemPromptBase,
    `Contextual rule for this turn:\n${contextualRule}`,
    selectedAffiliateContext,
    "User message:",
    userMessage
  ].join("\n\n");
}

async function sendMessage(text) {
  const cleanText = String(text || "").trim();

  if (!cleanText) return;
  if (cleanText.length > 2000) return;
  if (!chatBox || !userInput) return;

  const copy = translations[currentLanguage];
  const messageId = createMessageId();
  const intentRouting = classifyRoutingIntent(cleanText);
  let selectedIntent = detectConciergeIntent(cleanText);
  let selectedAffiliate = null;
  let resolvedEntity = null;
  let selectedOptimizationContext = null;
  let entityMatchDebug = normalizeTruthLayerMatchResult(null, 0);
  let entityResolutionSource = "fallback_prompt";
  let fallbackAttemptedOverride = false;

  appendMessage(cleanText, "user-message");
  userInput.value = "";
  userInput.disabled = true;
  if (sendBtn) sendBtn.disabled = true;

  const loadingId = appendMessage(copy.thinkingMessage, "bot-message loading");

  try {
    const affiliates = await loadConciergeAffiliates({ forceRefresh: true });
    const shouldResetEntityContext = isEntityContextResetIntent(cleanText);

    if (shouldResetEntityContext) {
      clearActiveEntity();
    }

    const activeEntityId = getActiveEntityId();
    const activeEntity = activeEntityId ? hydrateTruthLayerEntity(activeEntityId, "session_memory_request") : null;
    const mentionedEntityMatch = activeEntity ? normalizeTruthLayerMatchResult(activeEntity, 1) : findTruthLayerEntityInMessage(cleanText);
    entityMatchDebug = mentionedEntityMatch || entityMatchDebug;
    const mentionedEntity = activeEntity || (mentionedEntityMatch?.entity ? hydrateTruthLayerEntity(mentionedEntityMatch.entity.entityId, "message_match") : null);

    if (activeEntity) {
      selectedAffiliate = activeEntity;
      selectedIntent = selectedIntent || activeEntity.type;
      entityResolutionSource = "session";
      latestConciergeSelectionContext = null;
      askSantoriniTrafficLog("AskSantorini Session-State v1 follow-up entity reuse:", {
        sessionId: getConciergeSessionId(),
        currentEntityId: activeEntity.entityId,
        inheritedEntitySource: "reused"
      });
    } else if (mentionedEntity) {
      selectedAffiliate = mentionedEntity;
      selectedIntent = selectedIntent || mentionedEntity.type;
      entityResolutionSource = "new_match";
      setActiveEntity(mentionedEntity);
      latestConciergeSelectionContext = null;
    } else if (mentionedEntityMatch?.ambiguous) {
      selectedAffiliate = null;
      selectedIntent = selectedIntent || classifyUniversalCtaIntent(cleanText);
      entityResolutionSource = "fallback_prompt";
      latestConciergeSelectionContext = null;
    } else {
      selectedAffiliate = affiliates.length ? chooseConciergeAffiliate(selectedIntent, cleanText) : null;
      entityResolutionSource = selectedAffiliate?.entityId ? "new_match" : "fallback_prompt";
    }
    resolvedEntity = selectedAffiliate?.entityId
      ? hydrateTruthLayerEntity(selectedAffiliate.entityId, activeEntity ? "follow_up_request" : "resolved_request")
      : null;
    selectedAffiliate = resolvedEntity || selectedAffiliate;

    // EMERGENCY: always handled first
    if (intentRouting.routing_path === "emergency") {
      const finalResponse = await finalizeResponse({
        userMessage: cleanText,
        llmText: getEmergencyFallbackMessage(intentRouting.emergency_type),
        selectedIntent: "emergency_request",
        entity: resolvedEntity,
        entityMatchConfidence: entityMatchDebug?.confidence || 0,
        entityResolutionSource: resolvedEntity ? entityResolutionSource : "fallback_prompt",
        intentRouting,
        fallbackAttemptedOverride: false,
        fallbackTriggered: true,
        fallbackPathUsed: "emergency_guidance",
        className: "bot-message",
        messageId,
        onBeforeRender: () => {
          const loadingEl = loadingId ? document.getElementById(loadingId) : null;
          if (loadingEl) loadingEl.remove();
        }
      });
      askSantoriniTrafficLog("AskSantorini Emergency Layer v1:", {
        sessionId: getConciergeSessionId(),
        routing_path: intentRouting.routing_path,
        routing_path_locked: Boolean(intentRouting.routing_path_locked),
        detected_intent: "emergency",
        emergency_type: intentRouting.emergency_type,
        matched_emergency_keywords: intentRouting.matched_emergency_keywords || [],
        emergency_intent_confidence: intentRouting.emergency_intent_confidence || 0
      });
      latestInteractionEvent = buildEvent({
        userMessage: cleanText,
        botResponse: finalResponse.text,
        intent: "emergency",
        affiliate: "",
        eventType: "message",
        messageId
      });
      await sendEvent(latestInteractionEvent);
      return;
    }

    // LOCATION: only fallback if entity is missing
    if (intentRouting.routing_path === "location" && !resolvedEntity) {
      const finalResponse = await finalizeResponse({
        userMessage: cleanText,
        llmText: getLocationFallbackMessage(cleanText),
        selectedIntent: "navigation_request",
        entity: null,
        entityMatchConfidence: entityMatchDebug.confidence,
        entityResolutionSource,
        intentRouting,
        fallbackAttemptedOverride,
        locationFallbackTriggered: true,
        entityMissingLocationCase: true,
        fallbackTriggered: false,
        fallbackPathUsed: "",
        className: "bot-message",
        messageId,
        onBeforeRender: () => {
          const loadingEl = loadingId ? document.getElementById(loadingId) : null;
          if (loadingEl) loadingEl.remove();
        }
      });
      askSantoriniTrafficLog("AskSantorini Location Fallback Handler v1:", {
        sessionId: getConciergeSessionId(),
        routing_path: intentRouting.routing_path,
        routing_path_locked: Boolean(intentRouting.routing_path_locked),
        location_fallback_triggered: true,
        entity_missing_location_case: true,
        fallback_attempted_override: fallbackAttemptedOverride
      });
      latestInteractionEvent = buildEvent({
        userMessage: cleanText,
        botResponse: finalResponse.text,
        intent: finalResponse.intent,
        affiliate: "",
        eventType: "message",
        messageId
      });
      await sendEvent(latestInteractionEvent);
      return;
    }

    // GENERAL: bypass Truth Layer and CTA system, return direct LLM response
    if (intentRouting.routing_path === "general") {
      const finalResponse = await finalizeResponse({
        userMessage: cleanText,
        generateLlmText: async () => {
          const response = await fetch(workerUrl, {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify({
              message: cleanText,
              prompt: buildConciergePrompt(cleanText, null)
            })
          });
          let data = null;
          try {
            data = await response.json();
          } catch (jsonError) {
            throw new Error("Worker did not return valid JSON.");
          }
          if (!response.ok) {
            throw new Error(data?.error || "Worker request failed.");
          }
          return {
            text: data?.reply || copy.noReplyMessage,
            ctaDebug: data?.cta_debug || null,
            extraActions: Array.isArray(data?.cta) ? data.cta : []
          };
        },
        selectedIntent,
        entity: null,
        entityMatchConfidence: 0,
        entityResolutionSource: "llm_only",
        intentRouting,
        fallbackAttemptedOverride: false,
        fallbackTriggered: false,
        fallbackPathUsed: "",
        className: "bot-message",
        messageId,
        onBeforeRender: () => {
          const loadingEl = loadingId ? document.getElementById(loadingId) : null;
          if (loadingEl) loadingEl.remove();
        }
      });
      latestInteractionEvent = buildEvent({
        userMessage: cleanText,
        botResponse: finalResponse.text,
        intent: finalResponse.intent,
        affiliate: "",
        eventType: "message",
        messageId
      });
      await sendEvent(latestInteractionEvent);
      return;
    }

    // BUSINESS: fallback logic ONLY for business queries
    if (intentRouting.routing_path === "business") {
      const truthComplete = Boolean(resolvedEntity && hasTruthCompleteness(resolvedEntity));
      const fallbackAttemptedRoutingPath = (!activeEntity && mentionedEntityMatch?.ambiguous) || !resolvedEntity
        ? "business"
        : "";
      fallbackAttemptedOverride = getFallbackAttemptedRoutingOverride(intentRouting, fallbackAttemptedRoutingPath);
      const fallbackRequired = Boolean(
        !fallbackAttemptedOverride
        && (
          (intentRouting.routing_path === "business" && !activeEntity && mentionedEntityMatch?.ambiguous)
          || (isBusinessContactIntent(cleanText) && !resolvedEntity)
        )
      );
      if (fallbackRequired) {
        const sessionFallbackEntity = activeEntityId
          ? hydrateTruthLayerEntity(activeEntityId, "fallback_session_reuse")
          : null;
        if (sessionFallbackEntity) {
          resolvedEntity = sessionFallbackEntity;
          selectedAffiliate = sessionFallbackEntity;
          selectedIntent = selectedIntent || sessionFallbackEntity.type;
        }
        const fallbackPathUsed = activeEntityId && sessionFallbackEntity
          ? "session_reuse"
          : "clarification_request";
        entityResolutionSource = fallbackPathUsed === "session_reuse" ? "session" : "fallback_prompt";
        const ambiguousMatch = !activeEntity && mentionedEntityMatch?.ambiguous;
        const staticFallbackMessage = ambiguousMatch
          ? "I found more than one similar place. Please send the full business name so I can use only verified details."
          : getTruthLayerFallbackMessage(resolvedEntity?.missingFieldsList || truthLayerFactualFields, {
              entity: resolvedEntity,
              intent: selectedIntent,
              fallbackPathUsed
            });
        askSantoriniTrafficLog("AskSantorini Helpful Action Fallback Layer v1:", {
          sessionId: getConciergeSessionId(),
          sessionEntityId: activeEntityId,
          resolvedEntityId: resolvedEntity?.entityId || "",
          entity_resolution_source: entityResolutionSource,
          detected_intent_type: intentRouting.detected_intent_type,
          intent_confidence_score: intentRouting.intent_confidence_score,
          routing_path: intentRouting.routing_path,
          routing_path_locked: Boolean(intentRouting.routing_path_locked),
          fallback_attempted_override: fallbackAttemptedOverride,
          fallback_path_used: fallbackPathUsed,
          fallback_triggered: true
        });
        // Grounding-first fallback: when no verified entity but the user asked for contact info,
        // try Gemini Search Grounding via the worker before showing the static "no verified data" message.
        const useGroundingFallback = !ambiguousMatch;
        const finalResponse = await finalizeResponse({
          userMessage: cleanText,
          llmText: useGroundingFallback ? "" : staticFallbackMessage,
          generateLlmText: useGroundingFallback
            ? async () => {
                try {
                  const response = await fetch(workerUrl, {
                    method: "POST",
                    headers: { "Content-Type": "application/json" },
                    body: JSON.stringify({
                      message: cleanText,
                      prompt: buildConciergePrompt(cleanText, null)
                    })
                  });
                  const data = await response.json();
                  if (!response.ok) {
                    return { text: staticFallbackMessage, ctaDebug: null, extraActions: [] };
                  }
                  const replyText = String(data?.reply || "").trim();
                  const extraActions = Array.isArray(data?.cta) ? data.cta : [];
                  return {
                    text: replyText || staticFallbackMessage,
                    ctaDebug: data?.cta_debug || null,
                    extraActions
                  };
                } catch (groundingError) {
                  console.warn("AskSantorini grounding fallback failed:", groundingError);
                  return { text: staticFallbackMessage, ctaDebug: null, extraActions: [] };
                }
              }
            : null,
          selectedIntent,
          entity: fallbackPathUsed === "clarification_request" ? null : resolvedEntity,
          entityMatchConfidence: entityMatchDebug.confidence,
          entityResolutionSource,
          intentRouting,
          fallbackAttemptedOverride,
          fallbackTriggered: true,
          fallbackPathUsed,
          className: useGroundingFallback ? "bot-message" : "bot-message error",
          messageId,
          onBeforeRender: () => {
            const loadingEl = loadingId ? document.getElementById(loadingId) : null;
            if (loadingEl) loadingEl.remove();
          }
        });
        latestInteractionEvent = buildEvent({
          userMessage: cleanText,
          botResponse: finalResponse.text,
          intent: finalResponse.intent,
          affiliate: fallbackPathUsed === "session_reuse" ? selectedAffiliate : "",
          eventType: "message",
          messageId
        });
        await sendEvent(latestInteractionEvent);
        return;
      }
    }

    selectedOptimizationContext = latestConciergeSelectionContext;
    await sendAffiliateImpressionOnce({
      messageId,
      userMessage: cleanText,
      intent: selectedIntent,
      affiliate: selectedAffiliate,
      optimizationContext: selectedOptimizationContext
    });
    const workerMessage = buildConciergePrompt(cleanText, selectedAffiliate);
    const finalResponse = await finalizeResponse({
      userMessage: cleanText,
      generateLlmText: async () => {
        const response = await fetch(workerUrl, {
          method: "POST",
          headers: {
            "Content-Type": "application/json"
          },
          body: JSON.stringify({
            message: cleanText,
            prompt: workerMessage
          })
        });
        let data = null;

        try {
          data = await response.json();
        } catch (jsonError) {
          throw new Error("Worker did not return valid JSON.");
        }

        if (!response.ok) {
          throw new Error(data?.error || "Worker request failed.");
        }

        return {
          text: data?.reply || copy.noReplyMessage,
          ctaDebug: data?.cta_debug || null,
          extraActions: Array.isArray(data?.cta) ? data.cta : []
        };
      },
      selectedIntent,
      entity: resolvedEntity,
      entityMatchConfidence: entityMatchDebug.confidence,
      entityResolutionSource,
      intentRouting,
      fallbackAttemptedOverride,
      fallbackPathUsed: getActiveEntityId() ? "session_reuse" : "clarification_request",
      className: "bot-message",
      messageId,
      onBeforeRender: () => {
        const loadingEl = loadingId ? document.getElementById(loadingId) : null;
        if (loadingEl) loadingEl.remove();
      }
    });
    latestInteractionEvent = buildEvent({
      userMessage: cleanText,
      botResponse: finalResponse.text,
      intent: finalResponse.intent,
      affiliate: selectedAffiliate,
      eventType: "message",
      messageId,
      optimizationContext: selectedOptimizationContext
    });

    await sendEvent(latestInteractionEvent);

  } catch (error) {
    console.error("AskSantorini chat error:", error);

    const finalResponse = await finalizeResponse({
      userMessage: cleanText,
      llmText: copy.disconnectedMessage,
      selectedIntent,
      entity: resolvedEntity,
      entityMatchConfidence: entityMatchDebug.confidence,
      entityResolutionSource,
      intentRouting,
      fallbackAttemptedOverride,
      className: "bot-message error",
      messageId,
      onBeforeRender: () => {
        const loadingEl = loadingId ? document.getElementById(loadingId) : null;
        if (loadingEl) loadingEl.remove();
      }
    });
    latestInteractionEvent = buildEvent({
      userMessage: cleanText,
      botResponse: finalResponse.text,
      intent: finalResponse.intent,
      affiliate: selectedAffiliate,
      eventType: "message",
      messageId,
      optimizationContext: selectedOptimizationContext
    });
    await sendEvent(latestInteractionEvent).catch(() => {});

  } finally {
    userInput.disabled = false;
    if (sendBtn) sendBtn.disabled = false;
    userInput.focus();
  }
}

