function normalizeUrl(rawUrl) {
  const cleanUrl = String(rawUrl || "").trim();

  if (!cleanUrl) {
    return "";
  }

  try {
    if (/^tel:/i.test(cleanUrl)) {
      const phoneNumber = cleanUrl.replace(/^tel:/i, "").replace(/[^\d+]/g, "");

      return phoneNumber ? `tel:${phoneNumber}` : "";
    }

    const url = new URL(
      /^www\./i.test(cleanUrl) ? `https://${cleanUrl}` : cleanUrl,
      window.location.href
    );

    if (safeLinkProtocols.has(url.protocol)) {
      return url.href;
    }
  } catch {
    return "";
  }

  return "";
}

function normalizePhoneCandidate(rawPhone) {
  const digits = String(rawPhone || "").replace(/\D/g, "");

  if (!digits) {
    return "";
  }

  if (digits.length === 3 && ["100", "112", "166", "199"].includes(digits)) {
    return `tel:${digits}`;
  }

  if (digits.startsWith("30") && digits.length >= 10 && digits.length <= 12) {
    return `tel:+${digits}`;
  }

  if (digits.length >= 8 && digits.length <= 11) {
    return `tel:+30${digits}`;
  }

  return "";
}

function isTelephoneUrl(url) {
  return /^tel:\+?\d/i.test(url);
}

function isGoogleMapsUrl(url) {
  try {
    const parsedUrl = new URL(url);
    const host = parsedUrl.hostname.replace(/^www\./, "").toLowerCase();
    const path = parsedUrl.pathname.toLowerCase();

    return (
      host === "goo.gl" && path.startsWith("/maps") ||
      host === "maps.app.goo.gl" ||
      host === "maps.google.com" ||
      (host === "google.com" && path.startsWith("/maps"))
    );
  } catch {
    return false;
  }
}

function isPhoneContext(text, index, rawPhone) {
  const digits = String(rawPhone || "").replace(/\D/g, "");

  if (["100", "112", "166", "199"].includes(digits)) {
    return true;
  }

  if (emergencyIntentPattern.test(text)) {
    return true;
  }

  const contextStart = Math.max(0, index - 48);
  const contextEnd = Math.min(text.length, index + String(rawPhone || "").length + 48);

  return callIntentPattern.test(text.slice(contextStart, contextEnd))
    || emergencyIntentPattern.test(text.slice(contextStart, contextEnd));
}

function extractPhoneCandidates(text) {
  const sourceText = String(text || "");
  const candidates = [];
  const seen = new Set();
  const detectedBeforeGate = [];

  sourceText.replace(phoneCandidatePattern, (match, offset) => {
    const href = normalizePhoneCandidate(match);
    const status = {
      raw: match,
      href,
      index: offset,
      hasPhoneContext: isPhoneContext(sourceText, offset, match)
    };

    detectedBeforeGate.push(status);

    if (!href || seen.has(href)) {
      return match;
    }

    seen.add(href);
    candidates.push({
      raw: match,
      href,
      index: offset,
      hasPhoneContext: status.hasPhoneContext
    });

    return match;
  });

  askSantoriniTrafficLog("AskSantorini CTA debug - phone detected before gate:", detectedBeforeGate);
  askSantoriniTrafficLog("AskSantorini CTA debug - phone status after gate:", candidates);

  return candidates;
}

