function showAnswerPreview(card, shouldScroll = true) {
  if (!preview || !previewQuestion || !previewAnswer) {
    return;
  }

  const copy = translations[currentLanguage];
  const questionKey = card.dataset.questionKey;
  const question = copy.questions[questionKey];
  const answer = copy.answers[questionKey];

  if (!questionKey || !question || !answer) {
    return;
  }

  questionCards.forEach((item) => {
    item.classList.remove("is-active");
    item.setAttribute("aria-pressed", "false");
  });

  card.classList.add("is-active");
  card.setAttribute("aria-pressed", "true");

  previewQuestion.textContent = question;
  previewAnswer.textContent = answer;

  if (shouldScroll) {
    preview.scrollIntoView({ behavior: "smooth", block: "nearest" });
  }
}

questionCards.forEach((card, index) => {
  card.setAttribute("aria-pressed", "false");

  card.addEventListener("click", () => {
    showAnswerPreview(card);
  });

  if (index === 0) {
    card.classList.add("is-active");
    card.setAttribute("aria-pressed", "true");
  }
});

if (notifyForm) {
  const emailInput = notifyForm.querySelector("#email");

  emailInput?.addEventListener("input", () => {
    emailInput.setAttribute("aria-invalid", "false");

    if (formStatus) {
      formStatus.textContent = "";
    }
  });

  notifyForm.addEventListener("submit", (event) => {
    event.preventDefault();

    const copy = translations[currentLanguage];
    const formData = new FormData(notifyForm);
    const email = String(formData.get("email") || "").trim();
    const honeypot = String(formData.get("company") || "").trim();

    if (honeypot) {
      return;
    }

    if (!emailInput || !emailInput.checkValidity()) {
      emailInput?.setAttribute("aria-invalid", "true");

      if (formStatus) {
        formStatus.textContent = copy.invalidEmail;
      }

      emailInput?.focus();
      return;
    }

    emailInput.setAttribute("aria-invalid", "false");

    if (formStatus) {
      formStatus.textContent = copy.openingEmail;
    }

    const subject = encodeURIComponent(copy.mailSubject);
    const body = encodeURIComponent(copy.mailBody(email));

    window.location.href = `mailto:hello@asksantorini.ai?subject=${subject}&body=${body}`;
  });
}

modalButtons.forEach((button) => {
  button.addEventListener("click", (event) => {
    event.preventDefault();

    const modal = document.getElementById(button.dataset.modal);

    if (!modal) {
      return;
    }

    if (typeof modal.showModal === "function") {
      modal.showModal();
      document.body.classList.add("modal-open");
      return;
    }

    modal.setAttribute("open", "");
    document.body.classList.add("modal-open");
  });
});

