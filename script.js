  const headers = (headerRow || []).map((header) => header.trim().toLowerCase());
  const validatedRows = rows
    .map((values, index) => values.reduce((row, value, valueIndex) => ({ ...row, [headers[valueIndex]]: value.trim() }), { __rowNumber: index + 2 }))
