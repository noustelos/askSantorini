const SpeechRecognition = window.SpeechRecognition || window.webkitSpeechRecognition;


// WhatsApp-level continuous voice input UX
if (micBtn && userInput && SpeechRecognition) {
    const micStatus = document.getElementById("mic-status");
    function setMicStatus(state) {
      // Lightweight, non-intrusive status
      if (!micStatus) return;
      let txt = "";
      if (state === STATE.listening) txt = translations[currentLanguage]?.listeningAria || "Listening…";
      else if (state === STATE.processing) txt = "Processing…";
      else txt = "Ready";
      micStatus.textContent = txt;
      micStatus.className = "mic-status mic-status-" + (state === STATE.listening ? "listening" : state === STATE.processing ? "processing" : "ready");
    }
  // --- State Machine ---
  const STATE = { idle: 0, listening: 1, processing: 2 };
  let state = STATE.idle;
  let recognition = null;
  let autoRestartTimeout = null;
  let lastStableTranscript = "";

  function getCurrentSpeechLanguage() {
    const htmlLang = document.documentElement.lang || "en";
    const savedLang =
      getStoredValue(languageStorageKey) ||
      getStoredValue("askSantoriniLang") ||
      getStoredValue("language") ||
      "";
    const currentLang = (htmlLang || savedLang || "en").toLowerCase();
    if (currentLang.startsWith("el")) return "el-GR";
    return "en-US";
  }

  function setState(newState) {
    state = newState;
  }

  function resetMicButton() {
      setMicStatus(STATE.idle);
    const copy = translations[currentLanguage];
    micBtn.classList.remove("recording");
    micBtn.setAttribute("aria-label", copy.micAria);
    micBtn.textContent = "🎤";
  }

  function setMicListening() {
      setMicStatus(STATE.listening);
    micBtn.classList.add("recording");
    micBtn.setAttribute("aria-label", translations[currentLanguage].listeningAria);
    micBtn.textContent = "■";
  }

  function startRecognition() {
    if (autoRestartTimeout) {
      clearTimeout(autoRestartTimeout);
      autoRestartTimeout = null;
    }
    if (recognition) {
      recognition.onend = null;
      recognition.onerror = null;
      recognition.onresult = null;
      recognition.onaudioend = null;
      recognition.onnomatch = null;
      try { recognition.stop(); } catch {}
    }
    recognition = new SpeechRecognition();
    // Dictation mode: one-shot, auto-stops on silence (industry standard:
    // ChatGPT / Claude / Gemini / iOS keyboard mic).
    recognition.continuous = false;
    recognition.interimResults = true;
    recognition.maxAlternatives = 1;
    recognition.lang = getCurrentSpeechLanguage();

    // Snapshot of text that was already in the input before dictation began.
    // We APPEND the spoken transcript to it instead of overwriting.
    const baselineText = userInput.value;
    const needsSeparator = baselineText.length > 0 && !/\s$/.test(baselineText);
    const prefix = baselineText + (needsSeparator ? " " : "");

    recognition.onstart = () => {
      setState(STATE.listening);
      setMicListening();
      lastStableTranscript = userInput.value;
    };

    recognition.onresult = (event) => {
      let finalText = "";
      let interimText = "";
      for (let i = 0; i < event.results.length; ++i) {
        const res = event.results[i];
        if (res.isFinal) {
          finalText += res[0].transcript;
        } else {
          interimText += res[0].transcript;
        }
      }
      userInput.value = (prefix + finalText + interimText).replace(/\s+/g, " ").trimStart();
      if (userInput.value.trim()) {
        lastStableTranscript = userInput.value;
      }
    };

    recognition.onend = () => {
      // One-shot: do NOT auto-restart. Return to idle and let the user
      // review the text before pressing send.
      setState(STATE.idle);
      setMicStatus(STATE.idle);
      resetMicButton();
      if (userInput.value.trim()) {
        lastStableTranscript = userInput.value;
      }
    };

    recognition.onerror = (event) => {
      // Hard stop on permission / availability errors — no silent retry loop.
      setState(STATE.idle);
      setMicStatus(STATE.idle);
      resetMicButton();
      const fatal = event && (
        event.error === "not-allowed" ||
        event.error === "service-not-allowed" ||
        event.error === "audio-capture"
      );
      if (fatal) {
        // Restore prior text so the user doesn't lose their draft.
        userInput.value = baselineText;
      }
    };

    recognition.onspeechend = () => {
      // Let onend handle the state transition to keep behavior consistent.
      try { recognition.stop(); } catch {}
    };
  }

  micBtn.addEventListener("click", () => {
    if (state === STATE.idle) {
      setState(STATE.listening);
      setMicListening();
      setMicStatus(STATE.listening);
      startRecognition();
    } else {
      setState(STATE.idle);
      setMicStatus(STATE.idle);
      if (recognition) try { recognition.stop(); } catch {}
      resetMicButton();
      if (userInput.value.trim()) {
        lastStableTranscript = userInput.value;
      }
    }
  });

  // If user types or edits input, cancel voice
  userInput.addEventListener("input", () => {
    if (state !== STATE.idle) {
      setState(STATE.idle);
      setMicStatus(STATE.idle);
      if (recognition) try { recognition.stop(); } catch {}
      resetMicButton();
    }
  });

  // On page unload, cleanup
  window.addEventListener("beforeunload", () => {
    if (recognition) try { recognition.stop(); } catch {}
  });

} else if (micBtn) {
  micBtn.style.display = "none";
}

