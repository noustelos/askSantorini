async function fetchGoogleSheetAffiliates(sheetUrl) {
  if (!sheetUrl) return [];
  const csv = await fetch(sheetUrl).then((response) => response.ok ? response.text() : "").catch(() => "");
  if (!csv.trim()) return [];
  const [headerRow, ...rows] = parseCsvRows(csv.trim());
  const headers = (headerRow || []).map((header) => header.trim().toLowerCase());
  return rows.map((values) => values.reduce((row, value, index) => ({ ...row, [headers[index]]: value.trim() }), {}))
    .filter((row) => isActiveAffiliateRow(row))
    .map(normalizeAffiliateRow)
    .filter(Boolean)
    .sort((a, b) => b.score - a.score);
}

function parseCsvRows(csv) {
  const rows = [];
  let row = [];
  let field = "";
  let quoted = false;

  for (let index = 0; index < csv.length; index += 1) {
    const char = csv[index];
    const nextChar = csv[index + 1];
    if (char === '"' && quoted && nextChar === '"') {
      field += char;
      index += 1;
    } else if (char === '"') {
      quoted = !quoted;
    } else if (char === "," && !quoted) {
      row.push(field);
      field = "";
    } else if ((char === "\n" || char === "\r") && !quoted) {
      if (char === "\r" && nextChar === "\n") index += 1;
      row.push(field);
      rows.push(row);
      row = [];
      field = "";
    } else {
      field += char;
    }
  }

  row.push(field);
  rows.push(row);
  return rows;
}

function clampNumber(value, min, max) {
  const number = Number(value);
  if (!Number.isFinite(number)) return min;
  return Math.min(Math.max(number, min), max);
}

